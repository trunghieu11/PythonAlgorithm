import math


class PossibleOrders:
    def howMany(self, n, equal_info):
        self.partitions = [-1 for i in range(n)]
        for info in equal_info:
            val1, val2 = info.split("=")
            self.join(int(val1), int(val2))

        n = sum([1 if x < 0 else 0 for x in self.partitions])

        dp = [[0 for i in range(n + 1)] for j in range(n + 1)]
        dp[n][n] = dp[n][1] = dp[1][1] = 1
        for i in range(2, n + 1):
            for j in range(1, i + 1):
                dp[i][j] = j * dp[i - 1][j] + dp[i - 1][j - 1]

        answer = 0
        for i in range(1, n + 1):
            answer += math.factorial(i) * dp[n][i]
        return answer

    def root(self, x):
        if self.partitions[x] < 0:
            return x
        x = self.root(self.partitions[x])
        return x

    def join(self, x, y):
        x = self.root(x)
        y = self.root(y)
        if x == y:
            return
        if self.partitions[y] < self.partitions[x]:
            x, y = y, x
        self.partitions[x] += self.partitions[y]
        self.partitions[y] = x

